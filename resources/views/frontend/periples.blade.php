<h1>Périples</h1>
<div>
    la MAST, Mesure d’Accompagnement Scolaire Temporaire, du Lycée Professionnel Les Savarières à Saint-Sébastien, est dédiée aux mineurs étrangers de plus de 16 ans, allophones ou francophones et peu scolarisés antérieurement. Son action se décompose en 2 volets :

un volet scolaire qui propose de travailler les bases de l’alphabétisation et de la langue française, de la numération et du calcul.
un volet intégration qui propose des activités autour de la citoyenneté, des valeurs et des institutions de la République, de la découverte du monde professionnel et de la formation.
Dans le cadre d’une correspondance scolaire avec les 4è de Madame Doucet du collège d’Orvault cette année et avec le collège d’Estrées Saint-Denis (60) l’an dernier, les jeunes de la MAST se sont présentés et ont raconté leur périple pour venir en France.

Pour la semaine interculturelle et solidaire du Lycée Les Savarières, nous avons installé dans le hall du lycée une exposition appelée « Périples » à partir des extraits de ces échanges et d’un travail sur le portrait des jeunes.
